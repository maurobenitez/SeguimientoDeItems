    private void jBCambiarMouseClicked(java.awt.event.MouseEvent evt) {//GEN-FIRST:event_jBCambiarMouseClicked
        int indexEstado=jCBEstado.getSelectedIndex();
        int indexResponsable=jCBResponsable.getSelectedIndex();
        
        if ((indexEstado!=-1) && (indexResponsable!=-1)){
            Estado estado=listaEstados.get(indexEstado);
            Empleado responsable=listaEmpleados.get(indexResponsable);
            item.cambiarEstado(estado, responsable,usuario);
            Responsable.getInstance().actualizarItem();
            dispose();
        }
        else
            JOptionPane.showMessageDialog(null, "Campos no v√°lidos");
    }//GEN-LAST:event_jBCambiarMouseClicked

