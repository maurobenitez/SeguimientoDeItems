    private void jBCargarMouseClicked(java.awt.event.MouseEvent evt) {//GEN-FIRST:event_jBCargarMouseClicked
        if (esValido()){
            String nombre=jTFNickName.getText();
            Long dni=Long.parseLong(jTFDNI.getText(),10);
            Long legajo=Long.parseLong(jTFLegajo.getText());
            String contraseña=jTFContraseña.getText();
            Empleado empleado=new Empleado(legajo,nombre,dni,contraseña);
            controladorEmpleados.addElemento(empleado);
            dispose();
        }
    }//GEN-LAST:event_jBCargarMouseClicked

