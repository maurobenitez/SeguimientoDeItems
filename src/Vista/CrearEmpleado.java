    private static boolean esNumérico(String cadena){
    	try{
            Long.parseLong(cadena,10);
            return true;
    	} 
        catch(NumberFormatException nfe){
            return false;
    	}
    }
    
    public boolean esValido(){
        boolean validar=true;
        if ("".equals(jTFNickName.getText())) validar=false;
        if (!esNumérico(jTFDNI.getText())) validar=false;
        if (!esNumérico(jTFLegajo.getText())) validar=false;
        if ("".equals(jTFContraseña.getText())) validar=false;
        return validar;
    }
    
