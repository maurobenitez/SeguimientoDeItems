    private void jBAbrirProyectoActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jBAbrirProyectoActionPerformed
        if (usuarioValido()){
            if (proyectoValido()){
                if (proyecto.getLider()==empleado){
                    Admin.getInstance().abrirVentana(proyecto,empleado);
                    dispose();
                }

                else{
                    if (proyecto==empleado.getEquipo().getProyecto()){
                        Responsable.getInstance().abrirVentana(empleado.getEquipo(), empleado);
                        dispose();
                    }
                    else
                        JOptionPane.showMessageDialog(null, "No está asignado a este proyecto");
                }
            }
            else
                JOptionPane.showMessageDialog(null, "Seleccione un proyecto");

        }
        else
            JOptionPane.showMessageDialog(null, "Usuario o contraseña no válidos");
    }//GEN-LAST:event_jBAbrirProyectoActionPerformed

