    private boolean usuarioValido(){
        String nombre=jTFNombre.getText();
        String contraseña=jTFContraseña.getText();        
        Empleado emp=new Empleado(nombre);
        Collections.sort(empleados);
        int indexEmpleado=Collections.binarySearch(empleados, emp);
        if (indexEmpleado>=0){
            empleado=empleados.get(indexEmpleado);
            return empleado.getContraseña().equals(contraseña);              
        }
        else return false;
    }
    
    private boolean proyectoValido() {
        int indexProyecto=jCBProyectos.getSelectedIndex();
        if (indexProyecto!=-1){
            proyecto=modeloProyectos.getObjeto(indexProyecto);
            return true;
        }
        return false;
    }
    
    public void abrirVentana(){
        this.setLocationRelativeTo(null);
        this.setVisible(true);
        modeloProyectos.setElementos(Sistema.getInstance().getProyectos());        
        this.empleados=Sistema.getInstance().getEmpleados();
    }
    
