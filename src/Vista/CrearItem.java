    private boolean esNumérico(String cadena){
    	try{
            Long.parseLong(cadena,10);
            return true;
    	} 
        catch(NumberFormatException nfe){
            return false;
    	}
    }
    
    public boolean esValido(){
        Boolean esValido=true;
        if ("".equals(jTFNombre.getText())) esValido=false;
        if (!esNumérico(jTFId.getText())) esValido=false;
        if (jCBTipo.getSelectedIndex()==-1) esValido=false;
        if (jCBResponsable.getSelectedIndex()==-1) esValido=false;
        return esValido;
    }
