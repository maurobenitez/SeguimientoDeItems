    private void jBCrearMouseClicked(java.awt.event.MouseEvent evt) {//GEN-FIRST:event_jBCrearMouseClicked
        if (esValido()){
            Long id=Long.parseLong(jTFId.getText());
            String nombre=jTFNombre.getText();
            Tipo tipo=modeloTipos.getObjeto(jCBTipo.getSelectedIndex());
            Empleado responsable=modeloEmpleados.getObjeto(jCBResponsable.getSelectedIndex());
            Item i=new Item(id,nombre,tipo,responsable,empleado);
            controladorItems.addElemento(i);
            dispose();
        }
    }//GEN-LAST:event_jBCrearMouseClicked

